%%%=============================================================================
%%% attrs
%%%=============================================================================

attrs(Attrs) ->
  [[<<" ">>, atom_to_list(A), <<"=\"">>, maps:get(A, Attrs), <<"\"">>] || A <- maps:keys(Attrs)].

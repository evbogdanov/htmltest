-module(html).

-export([
  html/0, html/1, html/2,
  head/0, head/1, head/2,
  meta/0, meta/1, % self closing tag
  link/0, link/1, % self closing tag % !!! % overrides BIF link/1
  title/0, title/1, title/2,
  script/0, script/1, script/2,
  body/0, body/1, body/2,
  'div'/0, 'div'/1, 'div'/2,
  h1/0, h1/1, h1/2,
  p/0, p/1, p/2,
  a/0, a/1, a/2,
  ul/0, ul/1, ul/2,
  li/0, li/1, li/2

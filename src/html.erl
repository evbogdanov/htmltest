%%%=============================================================================
%%% html
%%%=============================================================================

html() ->
  <<"<!DOCTYPE html><html></html>">>.

html(Attrs) when is_map(Attrs) ->
  [<<"<!DOCTYPE html><html">>, attrs(Attrs), <<"></html>">>];
html(Content) ->
  [<<"<!DOCTYPE html><html>">>, Content, <<"</html>">>].

html(Attrs, Content) ->
  [<<"<!DOCTYPE html><html">>, attrs(Attrs), <<">">>, Content, <<"</html>">>].

